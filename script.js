// 生成星座运势数据
function generateConstellationFortune(constellation) {
    const data = constellationData[constellation];
    const today = new Date();
    const dateStr = today.toLocaleDateString('zh-CN', {
        year: 'numeric',
        month: 'long',
        day: 'numeric',
        weekday: 'long'
    });

    // 使用日期和星座名称作为种子，确保每个星座每天都有不同的运势
    const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();
    const constellationSeed = constellation.charCodeAt(0) + constellation.charCodeAt(constellation.length - 1);
    const combinedSeed = seed + constellationSeed;
    
    // 为每个运势类型生成不同的随机索引
    const careerIndex = (combinedSeed * 9301 + 49297) % 233280 % data.career.length;
    const loveIndex = (combinedSeed * 49297 + 9301) % 233280 % data.love.length;
    const wealthIndex = (combinedSeed * 233280 + 49297) % 233280 % data.wealth.length;
    const healthIndex = (combinedSeed * 49297 + 233280) % 233280 % data.health.length;
    
    // 为幸运信息生成不同的随机选择
    const colorIndex = (combinedSeed * 9301) % data.lucky.colors.length;
    const numberIndex = (combinedSeed * 49297) % data.lucky.numbers.length;
    const directionIndex = (combinedSeed * 233280) % data.lucky.directions.length;
    
    // 为整体运势等级生成索引
    const levelIndex = (combinedSeed * 9301 + 49297) % 233280 % fortuneLevels.length;
    
    // 为今日建议生成索引
    const adviceIndex = (combinedSeed * 49297 + 9301) % 233280 % dailyAdvices.length;

    return {
        date: dateStr,
        level: fortuneLevels[levelIndex],
        career: data.career[careerIndex],
        love: data.love[loveIndex],
        wealth: data.wealth[wealthIndex],
        health: data.health[healthIndex],
        luckyColor: data.lucky.colors[colorIndex],
        luckyNumber: data.lucky.numbers[numberIndex],
        luckyDirection: data.lucky.directions[directionIndex],
        advice: dailyAdvices[adviceIndex]
    };
}

// 显示星座运势
function showConstellationFortune(constellation) {
    const fortune = generateConstellationFortune(constellation);
    document.getElementById('selectedZodiacName').textContent = constellation;
    document.getElementById('currentDate').textContent = fortune.date;
    document.getElementById('fortuneLevel').innerHTML = `
        <span class="level-text">${fortune.level.stars}</span>
        <span class="level-desc">${fortune.level.desc}</span>
    `;
    document.getElementById('careerFortune').textContent = fortune.career;
    document.getElementById('loveFortune').textContent = fortune.love;
    document.getElementById('wealthFortune').textContent = fortune.wealth;
    document.getElementById('healthFortune').textContent = fortune.health;
    document.getElementById('luckyColor').textContent = fortune.luckyColor;
    document.getElementById('luckyNumber').textContent = fortune.luckyNumber;
    document.getElementById('luckyDirection').textContent = fortune.luckyDirection;
    document.getElementById('dailyAdvice').textContent = fortune.advice;
    document.getElementById('constellationSelection').style.display = 'none';
    document.getElementById('fortuneDisplay').style.display = 'block';
}


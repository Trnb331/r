// 页面初始化
function showSection(section) {
    document.getElementById('fortuneTypeSelection').style.display = 'none';
    document.getElementById('zodiacSelection').style.display = 'none';
    document.getElementById('constellationSelection').style.display = 'none';
    document.getElementById('fortuneDisplay').style.display = 'none';
    document.getElementById(section).style.display = 'block';
}

document.addEventListener('DOMContentLoaded', function() {
    // 运势类型选择
    document.querySelectorAll('.fortune-type-item').forEach(item => {
        item.addEventListener('click', function() {
            const type = this.getAttribute('data-type');
            if (type === 'zodiac') {
                showSection('zodiacSelection');
            } else if (type === 'constellation') {
                showSection('constellationSelection');
            }
        });
    });
    // 生肖选择
    document.querySelectorAll('.zodiac-item').forEach(item => {
        item.addEventListener('click', function() {
            const zodiac = this.getAttribute('data-zodiac');
            document.querySelectorAll('.zodiac-item').forEach(i => i.classList.remove('selected'));
            this.classList.add('selected');
            setTimeout(() => {
                showFortune(zodiac);
            }, 300);
        });
    });
    // 星座选择
    document.querySelectorAll('.constellation-item').forEach(item => {
        item.addEventListener('click', function() {
            const constellation = this.getAttribute('data-constellation');
            document.querySelectorAll('.constellation-item').forEach(i => i.classList.remove('selected'));
            this.classList.add('selected');
            setTimeout(() => {
                showConstellationFortune(constellation);
            }, 300);
        });
    });
    // 返回按钮
    document.getElementById('backBtn').addEventListener('click', function() {
        showSection('fortuneTypeSelection');
    });
    document.getElementById('backToTypeBtn1').addEventListener('click', function() {
        showSection('fortuneTypeSelection');
    });
    document.getElementById('backToTypeBtn2').addEventListener('click', function() {
        showSection('fortuneTypeSelection');
    });
    // 初始显示类型选择
    showSection('fortuneTypeSelection');
}); 